                                  It is a cooperation between <a href="http://mak.ac.ug/" target="_blank">Makerere University</a>  in Uganda (host institution),
                                  <a href="http://www.dit.ac.tz/" target="_blank">Dar es Salaam Institute of Technology</a>  (DIT) in Tanzania, the University of Juba in South Sudan and the Geophysical Institute of the University of Bergen.

                                Accessibility to reliable weather information is vital for decision making in various sectors such as agriculture, disaster management, aviation, fishing, energy, mining, construction, defense, water resources and health. The methods of weather prediction and meteorological observation currently being used in the East African region are outdated and a timely dissemination of weather information is more or less absent. This has resulted into challenges such as:</p>
                                <br>
                                <ul>
                                    <li>Low agricultural productivity.</li>
                                    <li>Deaths due to weather related diseases (e.g. Malaria).</li>
                                    <li>Delayed delivery times in the construction and energy industry.</li>
                                    <li>Loss of lives due to weather-related natural disasters (e.g. landslides, mudslides and floods) and accidents on  the lakes especially Lake Victoria where about 3000 people die every year.</li>

                                </ul>
                                <br>
                                <p>The project therefore aims to improve the <b>accuracy of and access to weather information</b>
                                     by the communities in the East African region through suitable ICTs (Information and Communication Technologies)
                                      for increased <b>productivity</b> (in the agricultural, energy, water resources and construction sectors) and safety
                                    (in the aviation, disaster management, fishing, health, mining, and defense sectors) and consists of 5 main components.</p>
